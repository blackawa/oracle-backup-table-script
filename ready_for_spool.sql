-- 見出し非表示
set head off
-- コマンドの結果出力を非表示
set termout off
-- 処理結果文の非表示
set feedback off
-- 置換文字列を使ったとき「旧: ... 新: ...」を非表示
set verify off
-- スクリプトから実行したとき、コマンドの実行結果を非表示
set echo off

-- 1行に許容する文字数を拡大
set linesize 10000
-- 行末の空白をトリムする
set trimspool on
-- 全部を1ページとして表示する
set pagesize 0
-- 結果を2000文字まで出力する(？)
set long 3000

